свободна_аудитория(День, Время, Аудитория) :-
    аудитория(Аудитория),
    \+ занятие(_, _, _, День, Время, Аудитория).

нагрузка_группы(Группа, КоличествоПар) :-
    группа(Группа),
    findall(_, занятие(Группа, _, _, _, _, _), Список),
    length(Список, КоличествоПар).

окна_в_расписании(Группа, День) :-
    группа(Группа),
    день(День),
    findall(Слот,
        ( занятие(Группа, _, _, День, Время, _),
          порядок_времени(Время, Слот) ),
        Слоты),
    sort(Слоты, ОтсортированныеСлоты),
    есть_пробел(ОтсортированныеСлоты).

есть_пробел([Прев, Тек | _]) :-
    Тек - Прев > 1,
    !.
есть_пробел([_, Тек | Хвост]) :-
    есть_пробел([Тек | Хвост]).

конфликт_группы(Группа, День, Время, Ауд1, Ауд2) :-
    занятие(Группа, _, _, День, Время, Ауд1),
    занятие(Группа, _, _, День, Время, Ауд2),
    Ауд1 @< Ауд2.

конфликт_преподавателя(Преп, День, Время, Гр1, Гр2) :-
    занятие(Гр1, _, Преп, День, Время, _),
    занятие(Гр2, _, Преп, День, Время, _),
    Гр1 @< Гр2.

конфликт_аудитории(Ауд, День, Время, Гр1, Гр2) :-
    занятие(Гр1, _, _, День, Время, Ауд),
    занятие(Гр2, _, _, День, Время, Ауд),
    Гр1 @< Гр2.

