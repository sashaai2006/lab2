запрос_1 :-
    целевая_группа(Г),
    целевой_предмет(Пред),
    format(atom(Заг), 'Запрос 1: Когда у группы ~w предмет ~w?', [Г, Пред]),
    заголовок(Заг),
    findall(День-Время, занятие(Г, Пред, _, День, Время, _), Все),
    sort(Все, Список),
    (   Список == []
    ->  writeln('  Не найдено')
    ;   forall(member(День-Время, Список),
               format('  ~w, ~w~n', [День, Время]))
    ).

