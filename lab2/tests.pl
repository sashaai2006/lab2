:- begin_tests(schedule).
:- consult('schedule.pl').

test(нет_конфликтов_группы) :-
    \+ конфликт_группы(_, _, _, _, _).

test(нет_конфликтов_преподавателя) :-
    \+ конфликт_преподавателя(_, _, _, _, _).

test(нет_конфликтов_аудитории) :-
    \+ конфликт_аудитории(_, _, _, _, _).

test(все_атомы_в_доменах) :-
    \+ атом_вне_домена(_, _).

test(база_проходит_общую_проверку) :-
    проверить_базу.

test(не_меньше_5_групп) :-
    aggregate_all(count, группа(_), N),
    assertion(N >= 5).

test(не_меньше_10_предметов) :-
    aggregate_all(count, предмет(_), N),
    assertion(N >= 10).

test(не_меньше_5_преподавателей) :-
    aggregate_all(count, преподаватель(_), N),
    assertion(N >= 5).

test(не_меньше_10_аудиторий) :-
    aggregate_all(count, аудитория(_), N),
    assertion(N >= 10).

test(пять_дней_недели) :-
    aggregate_all(count, день(_), N),
    assertion(N == 5).

test(свободна_аудитория_когда_нет_занятия) :-
    свободна_аудитория('Среда', '10:45', 'А101').

test(свободна_аудитория_когда_есть_занятие, [fail]) :-
    свободна_аудитория('Понедельник', '09:00', 'А101').

test(свободные_аудитории_перебираются) :-
    findall(А, свободна_аудитория('Среда', '10:45', А), Список),
    length(Список, N),
    assertion(N == 7),
    assertion(memberchk('А101', Список)),
    assertion(\+ memberchk('Б201', Список)).

test(нагрузка_501) :-
    нагрузка_группы('Б24-501', 10).

test(нагрузка_503) :-
    нагрузка_группы('Б24-503', 12).

test(сумма_нагрузок_равна_числу_занятий) :-
    findall(N, нагрузка_группы(_, N), Нагрузки),
    sum_list(Нагрузки, Сумма),
    findall(_, занятие(_, _, _, _, _, _), L),
    length(L, Всего),
    assertion(Сумма == Всего).

test(нагрузка_неизвестной_группы, [fail]) :-
    нагрузка_группы('Б24-999', _).

test(есть_окно_у_501_в_понедельник) :-
    окна_в_расписании('Б24-501', 'Понедельник').

test(есть_окно_у_501_во_вторник) :-
    окна_в_расписании('Б24-501', 'Вторник').

test(нет_окна_у_503_в_понедельник, [fail]) :-
    окна_в_расписании('Б24-503', 'Понедельник').

test(нет_окна_когда_только_одна_пара, [fail]) :-
    окна_в_расписании('Б24-501', 'Пятница').

test(нет_окна_у_504_во_все_дни) :-
    forall(день(Д), \+ окна_в_расписании('Б24-504', Д)).

test(порядок_времени_биекция) :-
    findall(В-Н, порядок_времени(В, Н), Все),
    length(Все, 4),
    findall(Н, порядок_времени(_, Н), Номера),
    sort(Номера, [1, 2, 3, 4]).

test(козлов_не_ведёт_занятий_в_четверг, [fail]) :-
    занятие(_, _, 'Козлов', 'Четверг', _, _).

test(запросы_исполняются_без_ошибок) :-
    consult('queries.pl'),
    with_output_to(string(_),
        ( запрос_1, запрос_2, запрос_3, запрос_4, запрос_5,
          запрос_6, запрос_7, запрос_8, запрос_9, запрос_10,
          запрос_11 )).

:- end_tests(schedule).
