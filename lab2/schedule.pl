атом_вне_домена(предмет,        П)  :- занятие(_, П, _, _, _, _), \+ предмет(П).
атом_вне_домена(преподаватель,  Пр) :- занятие(_, _, Пр, _, _, _), \+ преподаватель(Пр).
атом_вне_домена(день,           Д)  :- занятие(_, _, _, Д, _, _), \+ день(Д).
атом_вне_домена(время,          В)  :- занятие(_, _, _, _, В, _), \+ время(В).
атом_вне_домена(аудитория,      А)  :- занятие(_, _, _, _, _, А), \+ аудитория(А).

