форматировать_расписание([]) :-
    !,
    writeln('  Нет занятий').
форматировать_расписание(Строки) :-
    format('  ~w~t~16|~w~t~24|~w~t~36|~w~t~56|~w~n',
           ['День', 'Время', 'Группа', 'Предмет', 'Аудитория']),
    forall(member(порядок(Дн)-В-Г-Пр-А, Строки),
        ( номер_дня(Д, Дн),
          format('  ~w~t~16|~w~t~24|~w~t~36|~w~t~56|~w~n', [Д, В, Г, Пр, А]) )).

