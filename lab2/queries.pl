заголовок(Текст) :-
    nl,
    format('=== ~w ===~n', [Текст]).

вывести_список([], Пусто) :-
    !,
    format('  ~w~n', [Пусто]).
вывести_список(Список, _) :-
    forall(member(Эл, Список), format('  ~w~n', [Эл])).

склонение_пар(N, 'пара') :-
    N mod 100 =\= 11,
    N mod 10 =:= 1, !.
склонение_пар(N, 'пары') :-
    Сот is N mod 100,
    \+ (Сот >= 12, Сот =< 14),
    Дес is N mod 10,
    Дес >= 2,
    Дес =< 4, !.
склонение_пар(_, 'пар').

